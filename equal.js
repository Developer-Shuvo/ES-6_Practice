// == equal and === equal difference
// == equal: compares values for equality, allowing type coercion
// === equal: compares values for equality, without allowing type coercion



// == equal: its checking the value
// === equal: its checking the value and the type


const a = 5;
const b = [5];

if (a === b){
    console.log ('Values are equal');
}

else {
    console.log ('Values are not equal');
}